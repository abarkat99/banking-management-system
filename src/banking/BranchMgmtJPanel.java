    private void modifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyButtonActionPerformed
        if(bidTextField.getText()=="" || mbnameTextField.getText()=="" ||mbcityTextField.getText()==""||mbbalTextField.getText()=="")
            return;
        int bid=Integer.parseInt(bidTextField.getText());
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
        Statement stmt=conn.createStatement();
        ResultSet rs=stmt.executeQuery("SELECT count(*) FROM branch WHERE id="+bid);
        rs.next();
        if(rs.getInt(1)<1)
        {
            JOptionPane.showMessageDialog(parent,"Branch with id "+bid+" does not exist");
            return;
        }
        String query="{CALL MODIFYBRANCH(?,?,?,?)}";
        CallableStatement cstmt=conn.prepareCall(query);
        cstmt.setInt(1, bid);
        cstmt.setString(2,mbnameTextField.getText());
        cstmt.setString(3,mbcityTextField.getText());
        cstmt.setInt(4,Integer.parseInt(mbbalTextField.getText()));
        cstmt.executeQuery();
        JOptionPane.showMessageDialog(parent,"Updated Branch successfully");
        stmt.close();
        cstmt.close();
        conn.close();
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_modifyButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(bidTextField.getText()=="")
            return;
        int bid=Integer.parseInt(bidTextField.getText());
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
        Statement stmt=conn.createStatement();
        ResultSet rs=stmt.executeQuery("SELECT count(*) FROM branch WHERE id="+bid);
        rs.next();
        if(rs.getInt(1)<1)
        {
            JOptionPane.showMessageDialog(parent,"Branch with id "+bid+" does not exist");
            return;
        }
        stmt.executeUpdate("DELETE FROM branch WHERE id="+bid);
        JOptionPane.showMessageDialog(parent,"Deleted Branch successfully");
        stmt.close();
        conn.close();
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


