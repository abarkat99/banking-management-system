    private void selectlbidButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectlbidButtonActionPerformed
        // TODO add your handling code here:
        if(bidTextField.getText()=="")
            return;
        int bid=Integer.parseInt(bidTextField.getText());
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
        Statement stmt=conn.createStatement();
        ResultSet rs=stmt.executeQuery("SELECT name,city,balance FROM branch WHERE id="+bid);
        if(rs.next())
        {
            mbnameTextField.setText(rs.getString(1));
            mbcityTextField.setText(rs.getString(2));
            mbbalTextField.setText(Integer.toString(rs.getInt(3)));
        }
        else
        {
            JOptionPane.showMessageDialog(parent,"Branch with id "+bid+" does not exist");
            mbnameTextField.setText("");
            mbcityTextField.setText("");
            mbbalTextField.setText("");
        }
        stmt.close();
        conn.close();
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_selectlbidButtonActionPerformed

