    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try{
            String bname=bnameTextField.getText();
            String bcity=bcityTextField.getText();
            int bbal=Integer.parseInt(bbalTextField.getText());
            bnameTextField.setText("");
            bcityTextField.setText("");
            bbalTextField.setText("");
            if(bname==""||bcity==""||bbal<100000)
            {
                JOptionPane.showMessageDialog(parent,"Branch name or city cannot be null, balance must be greater than 100000");
                return;
            }
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
            String query="{CALL ADDBRANCH(?,?,?)}";
            CallableStatement cstmt=conn.prepareCall(query);
            cstmt.setString(1, bname);
            cstmt.setString(2, bcity);
            cstmt.setInt(3, bbal);
            ResultSet rs=cstmt.executeQuery();
            if(rs.next())
                JOptionPane.showMessageDialog(parent,"Branch Inserted with ID: "+rs.getInt(1));
            else
            {
                JOptionPane.showMessageDialog(parent,"Something went wrong");
            }
            cstmt.close();
            conn.close();
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_addButtonActionPerformed

