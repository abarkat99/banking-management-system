    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        long c_id=Long.parseLong(jTextField9.getText());
        long acc_id=Long.parseLong(jTextField1.getText());
        int amt=Integer.parseInt(jTextField2.getText());
        int interest=Integer.parseInt(jTextField3.getText());
        try{
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
            Statement stmt=conn.createStatement();
            ResultSet rs=stmt.executeQuery("SELECT COUNT(*) FROM cust_acc WHERE c_id="+c_id+" AND acc_id="+acc_id);
            rs.next();
            if(rs.getInt(1)<1)
            {
                JOptionPane.showMessageDialog(parent,"No such account with customer id is "+c_id+" and account id is "+acc_id);
                stmt.close();
                conn.close();
                return;
            }
            stmt.executeUpdate("INSERT into loan(amount,sanction_date,interest,c_id,acc_id) VALUES("+amt+", CURRENT_DATE, "+interest+", "+c_id+", "+acc_id+")");
            rs=stmt.executeQuery("SELECT LAST_INSERT_ID()");
            rs.next();
            JOptionPane.showMessageDialog(parent,"Loan sanctioned with id "+rs.getInt(1));
            stmt.close();
            conn.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

