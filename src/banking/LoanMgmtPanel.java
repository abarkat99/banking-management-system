    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int loan_id=Integer.parseInt(jTextField4.getText());
        try{
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/banking?zeroDateTimeBehavior=convertToNull","root","");
            Statement stmt=conn.createStatement();
            ResultSet rs=stmt.executeQuery("SELECT * FROM loan WHERE id="+loan_id);
            if(rs.next())
            {
                long c_id=rs.getLong(5);
                long acc_id=rs.getLong(6);
                int amt=rs.getInt(2);
                int interest=rs.getInt(4);
                jTextField10.setText(Long.toString(c_id));
                jTextField8.setText(Long.toString(acc_id));
                jTextField6.setText(Integer.toString(amt));
                jTextField5.setText(Integer.toString(interest));
            }
            else
                JOptionPane.showMessageDialog(parent,"No such loan with id "+loan_id);
            stmt.close();
            conn.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(parent,ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

